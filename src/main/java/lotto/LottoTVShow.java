package lotto;

import lotto.domain.Lotto;
import lotto.domain.LottoResult;

import java.util.ArrayList;
import java.util.List;

public class LottoTVShow {
	public static int purchaseAmount;

	public static int lottoTryCount;

	public static List<List<Integer>> autoGeneratedLottos = new ArrayList<>();

	public static List<Integer> lastWeekPrizeNumbers = new ArrayList<>();

	static Lotto lotto = new Lotto();

	public int calculateLottoCount(int purchaseAmount) {
		return purchaseAmount / Lotto.LOTTO_PRICE_PER_SHEET;
	}

	static List<Integer> compareLastWeekPrizeNumberWithAutoGeneratedLottoNumber() {
		List<Integer> lottoResultList = new ArrayList<>();
		for (List<Integer> autoGeneratedLotto : LottoTVShow.autoGeneratedLottos) {
			int matchCount = lotto.getMatchCount(LottoTVShow.lastWeekPrizeNumbers, autoGeneratedLotto);
			lottoResultList.add(matchCount);
		}
		return lottoResultList;
	}
}
